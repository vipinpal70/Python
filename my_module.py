import random       # import random module Here

def add(x, y):      # Define Add Function 
    return x + y    # return  Addition here

def subtract(x, y):     # Define  Subtraction Function 
    return x - y         # return Subtraction  here

def multiply(x, y):     # Define Multiplication Function 
    return x * y        # return Multiplication  here

def divide(x, y):        # Define Divide Function 
    return x / y         # return Division here

def generate_random_number():       # Define random Function 
    return random.randint(1, 100)    # return  random number here